
public class switch_case {

	public static void main(String[] args) {
     int a= 23;
     switch(a)
     {
     case 23:
    	 System.out.println("the value of x is 3");
     break;
     case 2:
    	 System.out.println("the value of x is 2");
     break;
     case 3:
    	 System.out.println("the value of x is 3");
     break;
     default:
    	 System.out.println("the value of x is other than 1,2,3");
     break;
     }
	}

}
